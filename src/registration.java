    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        try{
                String a="select count(pid) from registration";
            if (pid.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter patient id");
                return;
            }
    
            else if (pid.getText().equals(a)) {
                JOptionPane.showMessageDialog(this, "Value Out Of Range");
                return;  
            }
            
            int p = JOptionPane.showConfirmDialog(null," Are you sure want to delete ?","Confirmation",JOptionPane.YES_NO_OPTION);

            if (p==0)
            {
                con=connect.ConnectDB();
                String sql= "delete from registration where pid = '" + pid.getText() + "'";
                ps=con.prepareStatement(sql);
                ps.execute();
                JOptionPane.showMessageDialog(this,"Successfully deleted");
                reset();
            }
        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }   
    }//GEN-LAST:event_deleteActionPerformed

