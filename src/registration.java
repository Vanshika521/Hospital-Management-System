    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try{
            con=connect.ConnectDB();
            if (pid.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Enter patient id","Error", JOptionPane.ERROR_MESSAGE);
               //JOptionPane.showMessageDialog( this, "Please enter patient id");
                return;
            }
            if (name.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter Name ","Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (fname.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter Father's Name","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (address.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter Address","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (phone.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter Contact Number Name","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (email.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter E-mail ID","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (age.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter Age","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (gender.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter Gender","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
           if (bgrp.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter Blood Group","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
           if (info.getText().equals("")) {
                JOptionPane.showMessageDialog(this,"Enter Information","ERROR",JOptionPane.ERROR_MESSAGE);
                return;
            }
     
            Statement stmt;
            stmt= con.createStatement();
            String sql1="Select pid from registration where pid= " + pid.getText() ;
            rs=stmt.executeQuery(sql1);
     
           if(rs.next()){
                JOptionPane.showMessageDialog( this, "Patient ID already exists","Error", JOptionPane.ERROR_MESSAGE);
                pid.setText("");
                pid.requestDefaultFocus();
               return;
            }
            String sql= "insert into registration(pid,name,fname,address,contact,email,age,gender,bgrp,info)values('"+ pid.getText() + "','"+ name.getText() + "','"+ fname.getText() + "','"+ address.getText() + "','"+ phone.getText() + "','" + email.getText() + "','"+ age.getText() + "','" + gender.getSelectedItem() + "','"+ bgrp.getSelectedItem() + "','" + info.getText() + "')";
            ps=con.prepareStatement(sql);
            ps.execute();
            JOptionPane.showMessageDialog(this, "successfully done", "patient", JOptionPane.INFORMATION_MESSAGE);
            save.setEnabled(false);
                        
           
        }catch(SQLException | HeadlessException e){
            JOptionPane.showInternalMessageDialog(this, e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_saveActionPerformed

